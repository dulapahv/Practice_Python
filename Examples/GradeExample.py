score = float(input("What is your score?"))
if score >= 80:
    print("Grade A")
elif score >= 75:
    print("Grade B+")
elif score >= 70:
    print("Grade B")
elif score >= 65:
    print("Grade C+")
elif score >= 60:
    print("Grade C")
elif score >= 55:
    print("Grade D+")
elif score >= 50:
    print("Grade D")
else:
    print("Grade F")
